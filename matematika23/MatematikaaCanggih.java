package matematika23;

public class MatematikaaCanggih extends Matematikaa{

/*Modulus*/
    void modulus(int a, int b){
        System.out.println("Total dari" + a + "%" + b + "=" +(a%b));
    }
    void modulus(int a, int b, int c){
        System.out.println("Total dari" + a + "%" + b + "%" + c + "=" +(a%b%c));
    }
    void modulus(double a, double b){
        System.out.println("Total dari" + a + "%" + b + "=" +(a%b));
    }
    void modulus(double a, double b, double c){
        System.out.println("Total dari" + a + "%" + b + "+" + c + "=" +(a%b%c));
    }
    
}
